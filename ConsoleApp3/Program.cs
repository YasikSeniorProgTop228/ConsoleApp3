using System;

class Program
{
    static void Main(string[] args)
    {
        int score = 0; // Поточні бали
        string[] questions = new string[]
        {
            "Питання 1: Які кольори у прапору України?\n1. жовтий і блакитний\n2. червоний і зелений\n3. чорний і жовтий\n4. зелений і фіолетовий",
            "Питання 2: В якому місті знаходиться ДТЕУ?\n1. Вінниця\n2. Київ\n3. Львів\n4. Одеса",
            "Питання 3: Яка столиця України?\n1. Київ\n2. Харків\n3. Львів\n4. Одеса",
            "Питання 4: Скільки областей в Україні?\n1. 24\n2. 22\n3. 25\n4. 26",
            "Питання 5: Який рік незалежності України?\n1. 1991\n2. 1989\n3. 2000\n4. 1993"
        };

        int[] correctAnswers = { 1, 2, 1, 1, 1 }; // Номери правильних відповідей

        // Цикл для кожного питання
        for (int i = 0; i < questions.Length; i++)
        {
            Console.WriteLine(questions[i]); // Вивід питання
            Console.Write("Введіть номер вашої відповіді: ");
            int userAnswer;

            // Перевірка на правильність введення
            if (int.TryParse(Console.ReadLine(), out userAnswer))
            {
                // Перевірка на правильність відповіді
                if (userAnswer == correctAnswers[i])
                {
                    score += 100; // Додаємо бали за правильну відповідь
                    Console.WriteLine("Правильна відповідь");
                }
                else
                {
                    Console.WriteLine("Неправильна відповідь");
                    Console.WriteLine("Кінець програми.");
                    break; // Вихід з гри при неправильній відповіді
                }

                Console.WriteLine($"Поточні бали: {score}\n");
            }
            else
            {
                Console.WriteLine("Будь ласка, введіть коректний номер відповіді.");
            }
        }

        // Вивід підсумкового рахунку
        Console.WriteLine($"Ваша загальна кількість балів: {score}");
    }
}

